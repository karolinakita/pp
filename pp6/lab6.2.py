a=input("Podaj pierwszą liczbę całkowitą: ")
b=input("Podaj drugą liczbę całkowitą: ")

print("Wynik dodawania tych liczb to", (int(a)+int(b)))
print("Wynik odejmowania tych liczb to", (int(a)-int(b)))
print("Wynik mnożenia tych liczb to", (int(a)*int(b)))
print("Wynik dzielenia tych liczb to", (int(a)/int(b)))
print("Reszta z dzielenia tych liczb to", (int(a)%int(b)))
